# Fehlende Schlüssel in den Einstellungen werden mit Standardwerten ergänzt
def ensure_settings_keys(settings, defaults):
    for key, value in defaults.items():
        if key not in settings:
            settings[key] = value
        elif isinstance(value, dict):
            ensure_settings_keys(settings[key], value)

# Sicherstellen, dass die Character_main.json existiert und alle notwendigen Keys enthält
def ensure_character_main_json():
    file_path = DATA_DIR / "Character_main.json"
    defaults = TABLES_CHARACTERS_DEFAULTS["character_ID_01"]

    if not file_path.exists():
        # Erstelle ein Dict mit einem Beispielcharakter
        characters = {
            "character_ID_01": defaults.copy()
        }
        with open(file_path, "w", encoding="utf-8") as f:
            json.dump(characters, f, indent=2, ensure_ascii=False)
        log_info(f"Character_main.json created at {file_path} (with example character).")
        return

    # Datei existiert: Keys prüfen und ggf. ergänzen
    try:
        with open(file_path, "r", encoding="utf-8") as f:
            characters = json.load(f)
        changed = False
        for char_id, char in characters.items():
            for key, default_value in defaults.items():
                if key not in char:
                    char[key] = default_value
                    changed = True
        if changed:
            with open(file_path, "w", encoding="utf-8") as f:
                json.dump(characters, f, indent=2, ensure_ascii=False)
            log_info(f"Character_main.json: missing keys added at {file_path}.")
    except Exception as e:
        log_error(f"Error checking/adding keys in Character_main.json at {file_path}: {e}")

# Sicherstellen, dass eine Tabelle-Datei existiert und alle notwendigen Keys enthält
def ensure_table_file(filename, defaults):
    path = DATA_DIR / filename
    try:
        if not path.exists():
            with open(path, "w", encoding="utf-8") as f:
                json.dump(defaults, f, indent=2, ensure_ascii=False)
        else:
            # Ergänze neue Keys in bestehender Datei
            with open(path, "r", encoding="utf-8") as f:
                data = json.load(f)
            changed = False
            for key, value in defaults.items():
                if key not in data:
                    data[key] = value
                    changed = True
            if changed:
                with open(path, "w", encoding="utf-8") as f:
                    json.dump(data, f, indent=2, ensure_ascii=False)
    except Exception as e:
        log_exception(f"Error ensuring table file {path}", e)

# Validierung der Schlüssel in den Standardkonfigurationen
def validate_keys():
    lang_keys = [list(LANGUAGE_DEFAULTS[lang].keys()) for lang in LANGUAGE_DEFAULTS]
    first_keys = lang_keys[0]
    for keys in lang_keys[1:]:
        if keys != first_keys:
            log_error("LANGUAGE_DEFAULTS keys are not in the same order or not identical.")
            return False
    for theme_name, theme in THEMES_STYLES_DEFAULTS.items():
        theme_keys = set()
        for key in theme.keys():
            if key in theme_keys:
                log_error(f"Duplicate key '{key}' found in theme '{theme_name}'")
                return False
            theme_keys.add(key)
    for style_name, style in BASE_STYLE_DEFAULT.items():
        style_keys = set()
        for key in style.keys():
            if key in style_keys:
                log_error(f"Duplicate key '{key}' found in base style '{style_name}'")
                return False
            style_keys.add(key)
    return True

# Initialisierung der Benutzereinstellungen
def init_settings():
    first_start = not USER_SETTINGS_FILE.exists()
    settings = load_user_settings()
    ensure_settings_keys(settings, DEFAULT_SETTINGS)
    if first_start:
        current_resolution = get_monitor_resolution()
        settings.setdefault("monitor", {})
        settings["monitor"]["screen_max_resolution"] = current_resolution
        log_info(f"Initial screen_max_resolution set to {current_resolution}")
    else:
        current_resolution = get_monitor_resolution()
        max_resolution = settings.get("monitor", {}).get("screen_max_resolution")
        if max_resolution != current_resolution:
            settings["monitor"]["screen_max_resolution"] = current_resolution
            log_info(f"Updated screen_max_resolution to {current_resolution}")
    save_user_settings(settings)
    return settings

# Initialisierung der Dateien (Übersetzung, Design, Formularfelder, Basis-Design, ComboBox-Übersetzung)
def init_files(language, style_theme):
    translation_file = overwrite_translation_file(language)
    theme_file = overwrite_theme_file(style_theme)
    overwrite_form_fields_file()
    overwrite_base_style_file()
    combobox_translation_file = overwrite_combobox_translation_file(language)
    return translation_file, theme_file, combobox_translation_file

# Initialisierung des Designs
def init_theme(settings):
    if "gui" not in settings or "style_theme" not in settings["gui"]:
        settings.setdefault("gui", {})
        settings["gui"]["style_theme"] = DEFAULT_SETTINGS["gui"]["style_theme"]
    style_theme = settings["gui"]["style_theme"]
    settings["gui"]["file_path_gui"] = get_theme_path(style_theme)
    return style_theme

# Initialisierung der Sprache
def init_language(settings, os_language):
    settings.setdefault("general", {})
    language = settings["general"].get("language", os_language)
    if language not in LANGUAGE_DEFAULTS:
        log_error(f"Language '{language}' not found in LANGUAGE_DEFAULTS. Falling back to 'en'.")
        language = "en"
    settings["general"]["language"] = language
    settings["general"]["file_path_lang"] = get_translation_path(language)
    log_info(f"Using language: {language}")
    return language

