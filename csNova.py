# Die Systemsprache wird ermittelt
def get_os_language():
    lang_tuple = locale.getlocale()
    lang = lang_tuple[0] if lang_tuple and lang_tuple[0] else None
    if lang:
        lang_code = lang.split('_')[0]
        if lang_code in LANGUAGE_DEFAULTS:
            return lang_code
    return "en"

# Die Bildschirmauflösung wird ermittelt
def get_monitor_resolution():
    try:
        system = platform.system()
        if system == "Linux":
            output = subprocess.check_output("xrandr | grep '*'", shell=True).decode()
            resolution = output.split()[0]
            return resolution
        elif system == "Windows":
            from ctypes import windll
            user32 = windll.user32
            user32.SetProcessDPIAware()
            width = user32.GetSystemMetrics(0)
            height = user32.GetSystemMetrics(1)
            return f"{width}x{height}"
        elif system == "Darwin":
            try:
                import AppKit
                screen = AppKit.NSScreen.screens()[0]
                width = int(screen.frame().size.width)
                height = int(screen.frame().size.height)
                return f"{width}x{height}"
            except ImportError:
                log_error("AppKit (pyobjc) not installed. Run 'pip install pyobjc' on macOS.")
                return "1920x1080"
    except Exception as e:
        log_exception("Error detecting monitor resolution.", e)
        return "1920x1080"

# Die Benutzereinstellungen werden geladen oder mit Standardwerten initialisiert
def load_user_settings():
    if USER_SETTINGS_FILE.exists():
        try:
            with open(USER_SETTINGS_FILE, "r", encoding="utf-8") as f:
                settings = json.load(f)
            log_info("user_settings.json loaded successfully.")
            return settings
        except Exception as e:
            log_exception("Error loading user_settings.json", e)
    log_info("Using DEFAULT_SETTINGS as fallback for user_settings.")
    return DEFAULT_SETTINGS.copy()

# Die Benutzereinstellungen werden gespeichert
def save_user_settings(settings):
    try:
        with open(USER_SETTINGS_FILE, "w", encoding="utf-8") as f:
            json.dump(settings, f, indent=2, ensure_ascii=False)
        log_info("user_settings.json saved successfully.")
    except Exception as e:
        log_exception("Error saving user_settings.json", e)

