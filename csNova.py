@log_call
def get_os_language():
    lang_tuple = locale.getlocale()
    lang = lang_tuple[0] if lang_tuple and lang_tuple[0] else None
    if lang:
        lang_code = lang.split('_')[0]
        if lang_code in LANGUAGE_DEFAULTS:
            return lang_code
    return "en"

@log_call
def load_user_settings():
    if USER_SETTINGS_FILE.exists():
        try:
            with open(USER_SETTINGS_FILE, "r", encoding="utf-8") as f:
                settings = json.load(f)
            log_info("user_settings.json loaded successfully.")
            return settings
        except Exception as e:
            log_exception("Error loading user_settings.json", e)
    log_info("Using DEFAULT_SETTINGS as fallback for user_settings.")
    return DEFAULT_SETTINGS.copy()

@log_call
def get_monitor_info():
    try:
        import subprocess
        output = subprocess.check_output("xrandr | grep '*'", shell=True).decode()
        resolution = output.split()[0]
        dpi = 96.0  # Default, can be improved
        log_info(f"Monitor info detected: resolution={resolution}, dpi={dpi}")
        return resolution, dpi
    except Exception as e:
        log_exception("Error detecting monitor info", e)
        return "1920x1080", 96.0

@log_call
def calculate_scale_factor(resolution, dpi):
    try:
        scale = float(dpi) / 96.0
        log_info(f"Calculated scale factor: {scale}")
        return round(scale, 2)
    except Exception as e:
        log_exception("Error calculating scale factor", e)
        return 1.0

@log_call
def save_user_settings(settings):
    try:
        with open(USER_SETTINGS_FILE, "w", encoding="utf-8") as f:
            json.dump(settings, f, indent=2, ensure_ascii=False)
        log_info("user_settings.json saved successfully.")
    except Exception as e:
        log_exception("Error saving user_settings.json", e)

@log_call
def check_monitor_and_update(settings):
    resolution, dpi = get_monitor_info()
    scale_factor = calculate_scale_factor(resolution, dpi)
    monitor = settings.get("monitor", {})
    changed = (
        monitor.get("screen_resolution") != resolution or
        monitor.get("screen_dpi") != dpi or
        monitor.get("scale_factor") != scale_factor
    )
    if changed:
        monitor["screen_resolution"] = resolution
        monitor["screen_dpi"] = dpi
        monitor["scale_factor"] = scale_factor
        monitor["screen_resolution_changed"] = True
        settings["monitor"] = monitor
        save_user_settings(settings)
        log_info("Monitor settings updated and saved.")
    return settings

